str="I am a coder"
print(str.endswith("er"))             #Returns true if string ends with that substring
print(str.capitalize())               #Capitalizes the 1st character
print(str.find("coder"))              #Returns 1st index of first occurance
print(str.count("am"))                #Counts the number of occurance of substring in a string
print(str.replace("coder","hacker"))  #Replaces all occurances with new ("old","new")